public class eight {
    public static void main(String[] args) {
        int a=7;
        System.out.println("a="+a);
        System.out.println("a++="+(a++));
        System.out.println("a after post increment="+a);
        System.out.println("++a="+(++a));
        System.out.println("--a="+(--a));
        System.out.println("a--="+(a--));
        System.out.println("a after post decrement="+a);
    }
}
